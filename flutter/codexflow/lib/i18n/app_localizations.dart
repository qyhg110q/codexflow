import 'package:flutter/widgets.dart';

class AppLanguage {
  const AppLanguage({
    required this.code,
    required this.nativeName,
    required this.englishName,
  });

  final String code;
  final String nativeName;
  final String englishName;
}

class AppLocalizations {
  const AppLocalizations._(this.code);

  final String code;

  static const defaultCode = 'zh';

  static const languages = <AppLanguage>[
    AppLanguage(code: 'zh', nativeName: '中文', englishName: 'Chinese'),
    AppLanguage(code: 'en', nativeName: 'English', englishName: 'English'),
    AppLanguage(code: 'ja', nativeName: '日本語', englishName: 'Japanese'),
    AppLanguage(code: 'ko', nativeName: '한국어', englishName: 'Korean'),
    AppLanguage(code: 'es', nativeName: 'Español', englishName: 'Spanish'),
  ];

  static final supportedLocales = languages
      .map((language) => Locale(language.code))
      .toList(growable: false);

  static AppLocalizations of(String code) {
    return AppLocalizations._(isSupported(code) ? code : defaultCode);
  }

  static bool isSupported(String code) {
    return languages.any((language) => language.code == code);
  }

  static String languageName(String code) {
    for (final language in languages) {
      if (language.code == code) {
        return language.nativeName;
      }
    }
    return languages.first.nativeName;
  }

  Locale get locale => Locale(code);

  String t(
    String key, [
    Map<String, String> params = const <String, String>{},
  ]) {
    final language = _strings[code] ?? _strings[defaultCode]!;
    var value =
        language[key] ??
        (code == defaultCode ? null : _strings['en']?[key]) ??
        _strings[defaultCode]![key] ??
        key;
    for (final entry in params.entries) {
      value = value.replaceAll('{${entry.key}}', entry.value);
    }
    return value;
  }
}

